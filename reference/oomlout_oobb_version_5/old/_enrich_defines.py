# ── writer ────────────────────────────────────────────────────────────────────

def build_define_block(name: str, data: dict) -> str:
    aliases = data.get("shape_aliases", [])
    returns = data.get("returns", "List of geometry component dicts.")
    variables = data.get("variables", [])

    var_lines = ""
    for v in variables:
        var_lines += f'    v.append({{"name": {v["name"]!r}, "description": {v["description"]!r}, "type": {v["type"]!r}, "default": {v["default"]!r}}})\n'

    return (
        f'def describe():\n'
        f'    global d\n'
        f'    d = {{}}\n'
        f'    d["name"] = {name!r}\n'
        f'    d["name_long"] = {data["name_long"]!r}\n'
        f'    d["description"] = {data["description"]!r}\n'
        f'    d["category"] = {data["category"]!r}\n'
        f'    d["shape_aliases"] = {aliases!r}\n'
        f'    d["returns"] = {returns!r}\n'
        f'    v = []\n'
        f'{var_lines}'
        f'    d["variables"] = v\n'
        f'    return d\n'
        f'\n'
        f'\n'
        f'def define():\n'
        f'    global d\n'
        f'    if not isinstance(d, dict) or not d:\n'
        f'        describe()\n'
        f'    defined_variable = {{}}\n'
        f'    defined_variable.update(d)\n'
        f'    return defined_variable'
    )


# Match an optional existing describe() block followed by define() — idempotent re-runs
DEFINE_RE = re.compile(r'(?:def describe\(\):.*?\n\n+)?def define\(\):.*?(?=\ndef |\Z)', re.DOTALL)


def patch_file(path: Path, name: str, data: dict) -> bool:
    content = path.read_text(encoding="utf-8")
    new_block = build_define_block(name, data)
    new_content, count = DEFINE_RE.subn(new_block, content, count=1)
    if count == 0:
        print(f"  WARN: no define() found in {path}")
        return False
    path.write_text(new_content, encoding="utf-8")
    return True


