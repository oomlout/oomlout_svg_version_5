    root = Path(__file__).parent / "components"
    updated = 0
    skipped = 0
    for name, data in DEFINES.items():
        wp = root / name / "working.py"
        if not wp.exists():
            print(f"  SKIP (no file): {name}")
            skipped += 1
            continue
        if patch_file(wp, name, data):
            print(f"  OK: {name}")
            updated += 1
        else:
            skipped += 1

    print(f"\nDone: {updated} updated, {skipped} skipped.")
